# Make a long string
long_string = "This is a string that is very long. I don't like having to type it every single time."
other_string = "yes!"

# Print the long string, by referencing its variable
print(long_string)
uppercase_long_string = long_string.upper()
print(uppercase_long_string)
print(uppercase_long_string.lower())

# Print the other string, in all-caps
print(other_string.upper())

# Print the student name with capitalization
student_name = "amanda guo"
student_name = student_name.title()
# Print the student name, in a broken up list
print(student_name.split())

